package com.example.maxcalendar.bean;

public class Months {

    private int year;
    private int month;
    private int dayCount;
    private int firstDayDiff;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDayCount() {
        return dayCount;
    }

    public void setDayCount(int dayCount) {
        this.dayCount = dayCount;
    }

    public int getFirstDayDiff() {
        return firstDayDiff;
    }

    public void setFirstDayDiff(int firstDayDiff) {
        this.firstDayDiff = firstDayDiff;
    }
}
